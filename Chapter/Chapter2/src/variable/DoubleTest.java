package variable;

public class DoubleTest {
    
    public static void main(String[] args) {
        
        //실수는 double 자료형이 기본
        //double은 8byte float은 4byte
        
        //실수 리터럴은 double 타입으로 자동 처리된다.
        double dNum = 3.14;
        
        //double은 8byte float은 4byte
        float fNum = 3.14F; //식별자가 없으면
        
        System.out.println(dNum);
        System.out.println(fNum);
        
     
    }
}
